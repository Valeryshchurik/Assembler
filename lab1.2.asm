.MODEL TINY
.DATA
	CONST16 DB 16
	HEXSYM DB '0123456789ABCDEF'
	CODSYM DB 0
.CODE
.STARTUP
	MOV CX,256
	
PRINT_SYMBOL:
	PUSH CX
	MOV AX,40H
	MOV ES,AX
	MOV BH,ES:[62H]
	MOV AH,0AH
	MOV AL,CODSYM
	MOV CX,1
	INT 10H
	
	MOV AH,03H
	INT 10H
	INC DL
	MOV AH,02H
	INT 10H
	POP CX
	
	MOV DL,'-'
	MOV AH,06H
	INT 21H
	
	XOR AH,AH
	MOV AL,CODSYM
	DIV CONST16
	MOV BX,OFFSET HEXSYM
	XLAT
	XCHG AX,DX
	MOV AH,06H
	INT 21H
	
	MOV AL,DH
	XLAT
	MOV DL,AL
	MOV AH,06H
	INT 21H
	
	MOV DL,' '
	MOV AH,06H
	INT 21H

	ADD CODSYM,1
	LOOP PRINT_SYMBOL
	
	INT 20H
END